public class Escaping {
    public static void main(String[] args) {

        System.out.println("Name\t" + "Height\t" +"Gender");
        System.out.println("----------------------");
        System.out.println("Anna\t" + "5\'4\"\t" + "F");
        System.out.println("Test\t" + "6\'2\"\t" + "M");
        System.out.println("Ben \t" + "6\'2\"\t" + "M");
    }
}
