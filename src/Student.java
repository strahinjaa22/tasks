public class Student {
    String name;
    String lastName;
    int age;
    double gpa;
    boolean passedExam;

    public Student(String name,String lastName, int age, double gpa, boolean passedExam){
        this.name = name;
        this.lastName = lastName;
        this.age = age;
        this.gpa = gpa;
        this.passedExam = passedExam;
    }
}
